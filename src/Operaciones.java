public class Operaciones {

    public float sumar (float D1, float D2){
        return D1 + D2;
    }

    public float restar (float D1, float D2){
        return D1 - D2;
    }

    public float multiplicar (float D1, float D2){
        return D1 * D2;
    }

    public float dividir (float D1, float D2){
        return D1 / D2;
    }
    
    public float potencia (float D, float P){
        return (float) Math.pow(D,P);
    }


}
